class CException
{
public:
	CException(char* name)
	{	
		text = name;
	}
	~CException(void)
	{
	}

	char* Text(void){ return text; }

private:
	char* text;
};

//-----------------------------------------------------------------------------------------------
// better in this file then in precompile for example
//-----------------------------------------------------------------------------------------------
#define DECLARE_EXCEPTION(a)	class a : public CException								\
								{														\
								public:													\
									a(char* name) : CException(name){}					\
									~a(void){}												\
								};														\


