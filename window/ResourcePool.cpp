#include "StdAfx.h"
#include "ResourcePool.h"

//-----------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------
ResourcePool::ResourcePool(void)
{
	m_pool = POOL_DEFAULT;
}
//-----------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------
ResourcePool::~ResourcePool(void)
{
	items.clear(); 
}
//-----------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------
Int32 ResourcePool::AddResourceItem (ResourceItem* item)
{
	items.push_back(item);
	return (Int32)items.size();
}
//-----------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------
Int32 ResourcePool::GetResourceCount()
{
	return (Int32)items.size();
}
//-----------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------
void ResourcePool::SetPool(POOL pool)
{
	m_pool = pool;
}
//-----------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------
POOL ResourcePool::GetPool()
{
	return m_pool;
}
